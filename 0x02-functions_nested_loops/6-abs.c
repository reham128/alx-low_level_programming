/*
 * _abs - function that computes the absolute value of an integer number.
 *
 * Description: calculate the absolute value.
 *
 * @n: takes integer value for the function.
 *
 * Return: Always 0 (succes).
*/
int _abs(int n)
{
	if (n < 0)
	{
		n = (-1) * n;
	}
	else
		n == n;
	return (0);
}
