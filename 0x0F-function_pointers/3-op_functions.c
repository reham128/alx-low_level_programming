#include "3-calc.h"
/**
 * op_add - to add two integer numbers.
 * @a: first parameter.
 * @b: second parameter.
 * Return : a + b.
 */
int op_add(int a, int b)
{
	int result = a + b;

	return (result);
}
/**
 * op_sub - to subtract two integer numbers.
 * @a: first parameter.
 * @b: second parameter
 * Return : a - b.
 */
int op_sub(int a, int b)
{
	int result = a - b;

	return (result);
}
/**
 * op_mul - to multiply two integer numbers.
 * @a: first parameter
 * @b: second parameter
 * Return : a * b.
 */
int op_mul(int a, int b)
{
	int result = a * b;

	return (result);
}
/**
 * op_div - to divide two integer numbers.
 * @a: first parameter
 * @b: second parameter
 * Return : a / b.
 */
int op_div(int a, int b)
{
	int result = a / b;

	return (result);
}
/**
 * op_mod - to finde the reminder of division for two integer numbers.
 * @a: first parameter
 * @b: second parameter
 * Return : a % b.
 */
int op_mod(int a, int b)
{
	int result = a % b;

	return (result);
}
